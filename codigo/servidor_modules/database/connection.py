def get_database_path(project_root) -> Path:
    return Path(project_root) / "database" / "app.sqlite3"


def get_database_url(project_root=None) -> str:
    _load_environment_file(project_root)
    raw_url = str(os.environ.get("DATABASE_URL") or "").strip()
    if not raw_url:
        raise RuntimeError(
            "DATABASE_URL nao configurada. Defina a variavel de ambiente com a conexao PostgreSQL do Supabase."
        )
    return _enforce_ssl_mode(raw_url)


def get_connection(project_root):
    root_key = str(Path(project_root).resolve())
    with _POOL_LOCK:
        proxy = _PROXIES.get(root_key)
        if proxy is None:
            pool = ConnectionPool(
                conninfo=get_database_url(project_root),
                min_size=1,
                max_size=int(os.environ.get("DATABASE_POOL_MAX_SIZE", "10")),
                timeout=30,
                kwargs={
                    "autocommit": False,
                    "row_factory": dict_row,
                },
                open=True,
            )
            pool.wait()
            proxy = DatabaseConnectionProxy(pool)
            _POOLS[root_key] = pool
            _PROXIES[root_key] = proxy

    _initialize_database(project_root, root_key)
    return proxy


def execute_maintenance_statements(project_root, statements) -> None:
    root_key = str(Path(project_root).resolve())
    get_connection(project_root)
    pool = _POOLS[root_key]
    conn = pool.getconn()
    previous_autocommit = conn.autocommit

    try:
        try:
            conn.rollback()
        except Exception:
            pass

        conn.autocommit = True
        with conn.cursor() as cursor:
            for statement in statements or ():
                if str(statement or "").strip():
                    cursor.execute(str(statement))
    finally:
        try:
            conn.autocommit = previous_autocommit
        except Exception:
            pass

        try:
            if not conn.closed:
                pool.putconn(conn)
        except Exception:
            pass


def release_thread_connection(project_root=None) -> None:
    if project_root is not None:
        root_key = str(Path(project_root).resolve())
        proxy = _PROXIES.get(root_key)
        if proxy is not None:
            proxy.release()
        return

    for proxy in _PROXIES.values():
        proxy.release()


def start_connection_warmup(project_root) -> None:
    root_key = str(Path(project_root).resolve())
    warmup_lock = _WARMUP_LOCKS.setdefault(root_key, threading.Lock())

    with warmup_lock:
        if root_key in _WARMUP_STARTED_ROOTS:
            return
        _WARMUP_STARTED_ROOTS.add(root_key)

    warmup_thread = threading.Thread(
        target=_run_connection_warmup,
        args=(project_root,),
        daemon=True,
        name=f"postgres-warmup-{Path(root_key).name}",
    )
    warmup_thread.start()


def _run_connection_warmup(project_root) -> None:
    try:
        get_connection(project_root)
        release_thread_connection(project_root)
        print(" PostgreSQL pronto para uso.")
    except Exception as exc:
        print(f" Aviso no warmup do PostgreSQL: {exc}")


